public class User {

    String username;
    String Password;
    String forename;
    String surname;
    int employeeID;


    public User(String username, String password, String forename, String surname, int employeeID) {
        this.username = username;
        Password = password;
        this.forename = forename;
        this.surname = surname;
        this.employeeID = employeeID;
    }
}
